package com.itstudy.homework.oct20.faqiu2;

public class Football {
    String name;
    int num;

    public Football() {
    }

    public Football(String name, int num) {
        this.name = name;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
